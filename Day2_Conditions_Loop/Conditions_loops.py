# Day 2: Conditions & Loops

# Conditions--->
a = 10
b = 5

if a > b:
    print("a is greater than b")
elif a == b:
    print("a is equal to b")
else:
    print("b is greater than a")

# Loops--->

# For loop
print("For loop:")
for i in range(1, 6):
    print(i)

# While loop
print("While loop:")
i = 1
while i <= 5:
    print(i)
    i += 1